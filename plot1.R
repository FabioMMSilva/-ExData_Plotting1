library(data.table)
power_data<-fread("household_power_consumption.txt", colClasses="character")
power_data$Date<-as.Date(power_data$Date, format="%d/%m/%Y")
subpower<-subset(power_data, subset=(Date >="2007-02-01" & Date <="2007-02-02"))
globalactivepower<-as.numeric(subpower$Global_active_power)
hist(globalactivepower, xlab="Global Active Power (kilowatts)", ylab="Frequency", main="Global Active Power", col="red")
dev.copy(png, file="plot1.png", height=480, width=480)
dev.off()
