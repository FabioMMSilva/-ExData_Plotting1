mydata <- read.csv("household_power_consumption.txt", header=TRUE, sep=';', stringsAsFactors=FALSE, na.strings="?", check.names=FALSE, comment.char="", quote='\"')
mydata$Date <- as.Date(mydata$Date, format="%d/%m/%Y")
datasm <- subset(mydata, subset=(Date >= "2007-02-01" & Date < "2007-02-03"))
datetimeonly <- paste(as.Date(datasm$Date), datasm$Time)
datasm$Datetime <- as.POSIXct(datetimeonly)
plot(datasm$Global_active_power~datasm$Datetime, type="l",ylab="Global Active Power (kilowatts)", xlab="")
dev.copy(png, file="plot2.png", height=480, width=480)
dev.off()