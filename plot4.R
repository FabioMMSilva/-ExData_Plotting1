data <- read.table(file="household_power_consumption.txt", sep=";", skip=66637, nrows=2880, header=FALSE, stringsAsFactors=FALSE)
data$datetime <- paste(data[,1], data[,2], sep=" ")
Sys.setlocale("LC_TIME", "USA")
data$datetime <- strptime(data$datetime, format="%d/%m/%Y %H:%M:%S")
png(filename="plot4.png", width=480, height=480, antialias="cleartype")
par(mfrow=c(2,2), cex=0.8)
plot(data[,10], data[,3], type="l", ylab="Global Active Power", xlab="")
plot(data[,10], data[,5], type="l", ylab="Voltage", xlab="datetime")
plot(data[,10], data[,7], type="l", xlab="", ylab="Energy sub metering", cex=0.7)
points(data[,10], data[,8], type="l", col="red")
points(data[,10], data[,9], type="l", col="blue")
legend("topright",lwd=1,col=c("black", "red", "blue"), c("Sub_metering_1", "Sub_metering_2","Sub_metering_3"), seg.len=1, bg="transparent", box.col="transparent")
plot(data[,10], data[,4], type="l", ylab="Global_reactive_power", xlab="datetime")
dev.off()
