electric <- read.csv("household_power_consumption.txt", header = TRUE, sep = ';')
electric <- subset(electric, (electric$Date == "1/2/2007")|(electric$Date == "2/2/2007"))
electric$Global_active_power <- as.numeric(electric$Global_active_power)
Datetime <- strptime(paste(electric$Date, electric$Time), "%d/%m/%Y %H:%M:%S")
png("plot4.png", width = 480, height = 480)
par(mfrow = c(2,2))
plot(Datetime, electric$Global_active_power, xlab = "", ylab = "Global Active Power", type='l')
plot(Datetime, electric$Voltage, xlab = "", ylab = "Voltage", type='l')
plot(Datetime, electric$Sub_metering_1, xlab = "", ylab = "Energy sub metering", type='l')
lines(Datetime, electric$Sub_metering_2, xlab = "", ylab = "Energy sub metering", type='l', col="red")
lines(Datetime, electric$Sub_metering_3, xlab = "", ylab = "Energy sub metering", type='l', col="blue")
legend("topright", legend=c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"), col=c("black", "red", "blue"), lty=1, cex=1, box.lty=0)
plot(Datetime, electric$Global_reactive_power, xlab = "datetime", ylab = "Global_reactive_power", type='l')
dev.off()
