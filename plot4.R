household_power_consumption <- read.table("~/Coursera_ExplorData-CourseProj/household_power_consumption.txt", sep=";", quote="\"")
data<-household_power_consumption[as.Date(household_power_consumption$V1,format='%d/%m/%Y')>="2007-02-01" & as.Date(household_power_consumption$V1,format='%d/%m/%Y')<="2007-02-02",]
data$DateTime<-strptime(paste(data$Date, data$Time, sep=" "), format="%Y-%m-%d %H:%M:%S")
par(mfrow = c(2, 2), mar = c(4, 4, 2, 1), oma = c(0, 0, 2, 0))
with(data, {
  plot(data$DateTime,as.numeric(as.character(data$Global_active_power)),type="l",main="",xlab="",ylab="Global Active Power")
  plot(data$DateTime,as.numeric(as.character(data$Voltage)),type="l",main="",xlab="datetime",ylab="voltage")
  with(data, plot(DateTime, as.numeric(as.character(Sub_metering_1)),type="l",ylab="Energy sub metering",xlab=""))
  with(data, points(DateTime, as.numeric(as.character(Sub_metering_2)),col="red",type="l"))
  with(data, points(DateTime, as.numeric(as.character(Sub_metering_3)),col="blue",type="l"))
  legend("topright", pch = 1, col = c("black","blue", "red"), legend = c("Sub_metering_1", "Sub_metering_2","Sub_metering_3"),cex=.3)
  plot(data$DateTime,as.numeric(as.character(data$Global_reactive_power)),type="l",main="",xlab="",ylab="Global_reactive_power")
})
dev.copy(png,'plot4.png')
dev.off()