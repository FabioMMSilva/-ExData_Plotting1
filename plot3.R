h_p_c <- read.table(file = "household_power_consumption.txt", sep = ";", header = TRUE,stringsAsFactors = FALSE)
h_p_c <- unite(h_p_c, "DateTime", c("Date", "Time"), sep = " ")
h_p_c$DateTime <- strptime(h_p_c$DateTime, format = "%d/%m/%Y %H:%M:%S")
h_p_c <- na.omit(h_p_c)
h_p_c$DateTime <- as.POSIXct(h_p_c$DateTime)
h_p_c <- filter(h_p_c, h_p_c$DateTime > "2007-02-01" & h_p_c$DateTime < "2007-02-03")
png(file ="plot3.png", width = 480, height = 480)
plot(h_p_c$DateTime, h_p_c$Sub_metering_1, col = "black", type = "l", xlab =" ", ylab = "Energy Sub Metering", main = "Energy Sub Metering")
lines(h_p_c$DateTime, h_p_c$Sub_metering_2, col = "red")
lines(h_p_c$DateTime, h_p_c$Sub_metering_3, col = "blue")
legend("topright", legend = c("Sub_metering_1","Sub_metering_2","Sub_metering_3"), col = c("black", "red","blue"), lty = c(1,1,1))
dev.off()
