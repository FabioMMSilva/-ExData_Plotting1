a<-read.table("ExData_Plotting1\\household_power_consumption.txt",sep=";", header=T,stringsAsFactors = F)
c<-as.Date(as.character(a[[1]]),"%d/%m/%Y")
e<-a["2007-02-01"==c|"2007-02-02"==c,]
t<-paste(e$Date,e$Time)
t<-strptime(t, format="%d/%m/%Y %H:%M:%S")
dev.copy(png,"plot3.png")
plot(t,as.numeric(e$Sub_metering_1),,type ="n",xlab="",ylab= "Energy Sub metering")
points(t,as.numeric(e$Sub_metering_1),type ="l")
points(t,as.numeric(e$Sub_metering_2),type ="l", col ="red")
points(t,as.numeric(e$Sub_metering_3),type ="l", col ="blue")
legend("topright",col=c("black","red","blue"), legend = c("Sub_metering_1","Sub_metering_2","Sub_metering_3"),lty = 1)
dev.off()