Sys.setlocale("LC_TIME", "USA")
data$datetime <- strptime(data$datetime, format="%d/%m/%Y %H:%M:%S")
png(filename="plot3.png", width=480, height=480, antialias="cleartype")
plot(data[,10], data[,7], type="l", xlab="", ylab="Energy sub metering", cex=0.5)
points(data[,10], data[,8], type="l", col="red")
points(data[,10], data[,9], type="l", col="blue")
legend("topright",lwd=1,col=c("black", "red", "blue"), c("Sub_metering_1", "Sub_metering_2","Sub_metering_3"))
