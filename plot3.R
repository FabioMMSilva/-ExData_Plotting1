PowerConsData <- read.table("household_power_consumption.txt", skip=66637, nrows=2880, sep=";")
headernames <- read.table("household_power_consumption.txt", skip=0, nrows=1, sep=";", stringsAsFactors = FALSE)
colnames(PowerConsData) <- unlist(headernames)
daynames <- strptime(paste(PowerConsData$Date, PowerConsData$Time), "%d/%m/%Y %H:%M:%S")
par(mfrow=c(1,1))
with(PowerConsData, plot(daynames, Sub_metering_1, type="n", ylab="Energy sub metering", xlab=""))
lines(daynames, PowerConsData$Sub_metering_1, type="l", col="black")
lines(daynames, PowerConsData$Sub_metering_2, type="l", col="red")
lines(daynames, PowerConsData$Sub_metering_3, type="l", col="blue")
legend("topright", legend=headernames[7:9], lty=c(1,1,1), col=c("black", "red", "blue"))
dev.copy(png, file="plot3.png")
dev.off()